pub fn count_decimal_digit_64(n: u64) -> u32 {
    static POWERS_OF_10: [u64; 20] = [
        0,
        10,
        100,
        1_000,
        10_000,
        100_000,
        1_000_000,
        10_000_000,
        100_000_000,
        1_000_000_000,
        10_000_000_000,
        100_000_000_000,
        1_000_000_000_000,
        10_000_000_000_000,
        100_000_000_000_000,
        1_000_000_000_000_000,
        10_000_000_000_000_000,
        100_000_000_000_000_000,
        1_000_000_000_000_000_000,
        10_000_000_000_000_000_000,
    ];

    let t = ((64 - (n | 1).leading_zeros()) * 1233) >> 12;
    t - u32::from(n < *unsafe { POWERS_OF_10.get_unchecked(t as usize) }) + 1
}
